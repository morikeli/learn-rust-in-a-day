// Lesson 4 - variable data types
fn main() {
    // i32 - makes a variable a 32-bit signed integer
    // u32 - makes a variable a 32-bit unsigned integer
    // i64 - makes a variable a 64-bit signed integer
    // u64 - makes a variable a 64-bit unsigned integer
    // f32 - used in floating numbers in this case a 32-bit

    // NOTE: unsigned integers don't support negative numbers.

    let x: i64 = 45;    // i64
    let f: f32 = 6.7;    // f32
    let b: bool = false;

    print!("Value of x: {} | Value of f: {} | Value of b: {}", x, f, b)
    
}
